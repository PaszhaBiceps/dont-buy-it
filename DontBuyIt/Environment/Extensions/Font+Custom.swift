import SwiftUI

extension Font {
    static func robotoBlack(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Black", size: size)
    }
    
    static func robotoBlackItalic(_ size: CGFloat) -> Font {
        Font.custom("Roboto-BlackItalic", size: size)
    }
    
    static func robotoBold(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Bold", size: size)
    }
    
    static func robotoItalic(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Italic", size: size)
    }
    
    static func robotoLight(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Light", size: size)
    }
    
    static func robotoLightItalic(_ size: CGFloat) -> Font {
        Font.custom("Roboto-LightItalic", size: size)
    }
    
    static func robotoMedium(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Medium", size: size)
    }
    
    static func robotoMediumItalic(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Italic", size: size)
    }
    
    static func robotoRegular(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Regular", size: size)
    }
    
    static func robotoThin(_ size: CGFloat) -> Font {
        Font.custom("Roboto-Thin", size: size)
    }
    
    static func robotoThinItalic(_ size: CGFloat) -> Font {
        Font.custom("Roboto-ThinItalic", size: size)
    }
}
